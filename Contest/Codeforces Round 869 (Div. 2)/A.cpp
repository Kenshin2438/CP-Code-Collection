using ll = long long;
#define all(a) begin(a), end(a)
#define sz(x) (int)((x).size())

void solve() {
  int n, k; cin >> n >> k;
  vector<string> S(n);
  for (auto &s : S) cin >> s;

  auto mine = S[0];
  cout << count(all(S), mine) << '\n';
}

int main() {
  cin.tie(nullptr)->sync_with_stdio(false);
  cout << fixed << setprecision(15);

  int T = 1;
  cin >> T;
  while (T--) solve();

  return 0;
}